    private void pesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesanActionPerformed
        // TODO add your handling code here:
        String Nama = nama.getText();
        String NIK = nik.getText();
        String Gender = gender.getSelectedItem().toString();

        //membaca data kereta yang sudah dipilih
        String Kereta = FormCariKapal.Kereta;
        String Rute = FormCariKapal.Rute;
        String Tanggal = FormCariKapal.Tanggal;
        String Jam = FormCariKapal.Jam;
        int Harga = FormCariKapal.Harga;
        int IdTiket = FormCariKapal.IdTiket;
        

        //membaca waktu saat proses pembelian tiket
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy-MM-dd hh:mm");
        LocalDateTime WktBeliTiket = LocalDateTime.now();

        //simpan data penumpang dalam tabel 'penumpang'
        try{
            //membuat koneksi ke MYSQL dengan memanggil objek KoneksiDB
            Connection KoneksiDB = koneksiDB.getKoneksiDB();

            //membuat query untuk menyimpan data ke DB
            String query = "INSERT INTO penumpang (nik,nama,gender) VALUES (?,?,?) ";
            String query2 = "INSERT INTO riwayatpembelian (id_tiket,nik,tgl_beli) VALUES (?,?,?) ";

            PreparedStatement p = KoneksiDB.prepareStatement(query);
            PreparedStatement p2 = KoneksiDB.prepareStatement(query2);

            //menyiapkan data yang ingin disimpan ke tabel penumpang
            p.setString(1,NIK);
            p.setString(2,Nama);
            p.setString(3,Gender);

            //menyiapkan data yang ingin disimpan ke tabel Membeli
            p2.setInt(1,IdTiket);
            p2.setString(2,NIK);
            p2.setString(3, dtf.format(WktBeliTiket));

            //eksekusi query
            p.executeUpdate();
            p2.executeUpdate();

            //membuat tabel model dan menampilkan data ke dalam tabel
            if(Kereta != null){
                DefaultTableModel model = (DefaultTableModel) tabel.getModel();
                Object[] data = {Nama, NIK, Kereta, Rute, Tanggal, Jam, Harga};
                model.addRow(data);
            }

            //menutup koneksi
            p.close();

        } catch (SQLException e){
            String pesan = "data gagal disimpan";
            JOptionPane.showMessageDialog(null, pesan, "Kesalahan", JOptionPane.INFORMATION_MESSAGE);
        }

           //mengosongkan lagi semua field
        nik.setText(null);
        nama.setText(null);
        gender.setSelectedIndex(0);
        berangkat.setText(null);
        tujuan.setText(null);
        kereta.setText(null);
    }//GEN-LAST:event_pesanActionPerformed

