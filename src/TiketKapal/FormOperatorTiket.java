    private void TampilData(){
        try {
            Statement st =(Statement)koneksiDB.getKoneksiDB().createStatement();
            rs=st.executeQuery("select * from kereta INNER JOIN tiket ON tiket.nama_kereta = kereta.nama_kereta INNER JOIN rute ON tiket.id_rute = "
                    + "rute.id_rute INNER JOIN membeli ON tiket.id_tiket = "
                    + "membeli.id_tiket INNER JOIN penumpang ON membeli.nik = penumpang.nik");
            while(rs.next()){
                String _nama = rs.getString("penumpang.nama");
                String _nik = rs.getString("penumpang.nik");
                String _kereta = rs.getString("kereta.nama_kereta");
                String _berangkat = rs.getString("rute.berangkat");
                String _tujuan = rs.getString("rute.tujuan");
                String _tanggal = rs.getString("tiket.waktu").substring(0,10);
                String _jam = rs.getString("tiket.waktu").substring(11,16);
                String _rute = _berangkat +"-"+ _tujuan;
      
                DefaultTableModel model = (DefaultTableModel) tabel.getModel();
                Object[] data = {_nama,_nik,_kereta,_rute,_tanggal,_jam};
                model.addRow(data);
                }
            rs.close();
            st.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Error");
        }
        
    }

